const width = map[0].length;

const mapSplitted = map.map(row => row.split(""));


const pos = {
  x: 0,
  y: 0
}
const treeCounts = [];

slopes.map(slope => {
  let treeCount = 0;
  pos.x = 0;
  pos.y = 0;

  while(pos.y < height - 1) {
    pos.x = pos.x + slope.r;
    pos.y = pos.y + slope.d;
  
    if(pos.x >= width) {
      pos.x = pos.x - width;
    }
    
    const position = mapSplitted[pos.y][pos.x];
    
    if(position === "#") {
      treeCount++;
    }
  }
  treeCounts.push(treeCount);
});

const total = treeCounts.reduce((acc, c) => {
  acc = acc * c;

  return acc;
}, 1);

